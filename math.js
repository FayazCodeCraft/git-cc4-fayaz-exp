/**
 * Adds two numbers a and b
 * @param {number} a 
 * @param {number} b 
 * @returns the result
 */
function add(a,b)
{
    return a+b;
}
/**
 * Divides a and b
 * @param {number} a 
 * @param {number} b 
 * @returns the result
 */
function divide(a,b){
    return a/b;
}